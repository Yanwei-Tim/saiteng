package com.saiteng.st_lc32xcam.control;

public class WifiSSIDCmdInfo {
	private int code;
	private String ssid;
	private String resp;
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getSsid() {
		return ssid;
	}
	public void setSsid(String ssid) {
		this.ssid = ssid;
	}
	public String getResp() {
		return resp;
	}
	public void setResp(String resp) {
		this.resp = resp;
	}

}
